export function validateMatches(matchedData: { [serial: number]: string | undefined }) {
  console.info('\n\n\nValidating matches...\n');
  const matches = Object.entries(matchedData)
    .filter(entry => !!entry[1])
    .sort((a, b) => {
      return Number(a[0]) - Number(b[0]);
    });
  for (const [serial, match] of matches) {
    if (!!match && confirmedMatches[serial] === undefined) {
      console.log(`'${serial}': '${match}', // NOTE: local amount mismatch`);
      confirmedMatches[serial] = match;
      continue;
    } else if (!!confirmedMatches[serial] && confirmedMatches[serial] !== (match ?? null)) {
      throw new Error(`Match for serial ${serial} is wrong: ${match}`);
    }
  }
}
